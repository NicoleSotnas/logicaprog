/*Desenvolver um programa que apresente os resultados de uma tabela de um número qualquer. Ela deve ser
impressa no seguinte formato: */

let cont = 1;
let num = 2;
multi = 0; 
res = 0;

while( cont <= 10){
    multi += 1
    res = num * multi 
    console.log(`${num} . ${multi} = ${res}`)
    cont += 1
}